#include "libft.h"

int		ft_intlen(long long n)
{
	int l;

	l = n == 0 ? 1 : 0;
	n *= (n < 0) ? (-1) : 1;
	while (n > 0)
	{
		n /= 10;
		l++;
	}
	return (l);
}
